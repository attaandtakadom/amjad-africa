let totalCorrect = 0;
let totalAnswered = 0;
const totalAddedQuestions = 29; // إجمالي الأسئلة المستخرجة من الصور

/**
 * دالة التحديث الفوري للواجهة
 */
function refreshDisplay() {
    const answeredElem = document.getElementById('answered-count');
    const correctElem = document.getElementById('correct-count');
    const totalElem = document.getElementById('total-count');

    if (totalElem) totalElem.innerText = totalAddedQuestions;
    if (answeredElem) answeredElem.innerText = totalAnswered;
    if (correctElem) correctElem.innerText = totalCorrect;
}

/**
 * دالة معالجة الإجابة
 * تُستدعى عند قيام الطالب (مثل معتز أو معاذ) باختيار إجابة
 */
function onUserAnswer(isCorrect) {
    totalAnswered++; // زيادة العداد الإجمالي للمحاولات
    
    if (isCorrect === true) {
        totalCorrect++; // زيادة عداد الإجابات الصحيحة
    }
    
    // تحديث الأرقام على الشاشة فوراً
    refreshDisplay();
}

// تأكيد التشغيل عند تحميل الصفحة لضمان ظهور رقم 29
document.addEventListener('DOMContentLoaded', refreshDisplay);